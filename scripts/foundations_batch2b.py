    "## Indexes — How Lookups Get Fast",
    "",
    "```",
    "WITHOUT INDEX:",
    "  SELECT * FROM users WHERE email = 'alice@example.com';",
    "  → DB scans EVERY row checking emails. O(n) — slow for big tables.",
    "",
    "WITH INDEX on email:",
    "  → DB walks a B-tree (~log n) to find row. O(log n).",
    "",
    "  For 1M users:",
    "  Without index: 1M comparisons (~100ms)",
    "  With index:    20 comparisons (~0.1ms) → 1000x faster!",
    "",
    "INDEX = a sorted data structure (B-tree typically) on one or more columns.",
    "  Stored separately from the table.",
    "  Updated automatically on INSERT/UPDATE/DELETE.",
    "",
