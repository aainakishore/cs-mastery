    "## ACID Transactions",
    "",
    "ACID is the foundation of database trustworthiness:",
    "",
    "```",
    "ATOMICITY:",
    "  All-or-nothing. Either ALL changes commit, or NONE do.",
    "  Money transfer: deduct from A AND add to B — both happen, or neither.",
    "",
    "CONSISTENCY:",
    "  DB never violates rules (constraints, foreign keys, types).",
    "  Failed transaction rolls back; constraints prevent invalid states.",
    "",
    "ISOLATION:",
    "  Concurrent transactions don't interfere.",
    "  Reading half-applied data of another transaction = bad. Isolation prevents.",
    "  Levels: READ UNCOMMITTED (loosest), READ COMMITTED (default), ",
    "          REPEATABLE READ, SERIALIZABLE (strictest, slowest).",
    "",
    "DURABILITY:",
    "  Once committed, data survives crashes (written to disk).",
    "  Power outage right after COMMIT? Data is safe.",
    "",
    "EXAMPLE TRANSACTION:",
    "  BEGIN;",
    "    UPDATE accounts SET balance = balance - 100 WHERE id = 1;",
    "    UPDATE accounts SET balance = balance + 100 WHERE id = 2;",
    "    -- if any error, ROLLBACK auto",
    "  COMMIT;",
    "",
    "  If second UPDATE fails: BEGIN..ROLLBACK undoes the first too.",
    "  No partial state ever seen by other transactions.",
    "```",
