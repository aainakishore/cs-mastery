    "## Window Functions",
    "",
    "```",
    "Window functions operate on a 'window' of rows related to the current row",
    "WITHOUT collapsing them (unlike GROUP BY which collapses rows into one).",
    "",
    "GROUP BY:   many rows → one row per group   (aggregates, loses detail)",
    "Window fn:  every row kept + window computed value added beside it",
    "```",
    "",
    "```sql",
    "SELECT",
    "  salesperson,",
    "  region,",
    "  sales,",
    "  -- Rank within each region (restarts at 1 for each region)",
    "  RANK()        OVER (PARTITION BY region ORDER BY sales DESC) AS region_rank,",
    "  -- Row number (no ties)",
    "  ROW_NUMBER()  OVER (PARTITION BY region ORDER BY sales DESC) AS row_num,",
    "  -- Dense rank (no gaps: 1,2,2,3 not 1,2,2,4)",
    "  DENSE_RANK()  OVER (PARTITION BY region ORDER BY sales DESC) AS dense_rank,",
    "  -- Running total of sales within region",
    "  SUM(sales)    OVER (PARTITION BY region ORDER BY sales DESC",
    "                      ROWS UNBOUNDED PRECEDING)               AS running_total,",
    "  -- Previous row's sales value",
    "  LAG(sales, 1) OVER (PARTITION BY region ORDER BY sales DESC) AS prev_sales,",
    "  -- Next row's sales value",
    "  LEAD(sales, 1) OVER (PARTITION BY region ORDER BY sales DESC) AS next_sales",
    "FROM sales_data;",
    "",
    "-- Get only top earner per region:",
    "SELECT * FROM (",
    "  SELECT *,",
    "    RANK() OVER (PARTITION BY region ORDER BY sales DESC) AS rk",
    "  FROM sales_data",
    ") sub WHERE rk = 1;",
    "```",
    "",
    "```",
    "Window function anatomy:",
    "",
    "  FUNCTION_NAME(expression)",
    "  OVER (",
    "    PARTITION BY column   -- like GROUP BY but doesn't collapse rows",
    "    ORDER BY column       -- sort order for window frame",
    "    ROWS/RANGE BETWEEN    -- how many rows back/forward to include",
    "  )",
    "",
    "Common window functions:",
    "  ROW_NUMBER()  : unique sequential number (no ties)",
    "  RANK()        : rank with gaps on ties (1,2,2,4)",
    "  DENSE_RANK()  : rank without gaps (1,2,2,3)",
    "  LAG(col, n)   : value of col n rows before current",
    "  LEAD(col, n)  : value of col n rows after current",
    "  SUM/AVG/COUNT : running aggregate over window",
    "  FIRST_VALUE() : first value in window",
    "  LAST_VALUE()  : last value in window",
