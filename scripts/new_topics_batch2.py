    "## Transactions and Isolation Levels",
    "",
    "```sql",
    "BEGIN;",
    "  UPDATE accounts SET balance = balance - 100 WHERE id = 1;",
    "  UPDATE accounts SET balance = balance + 100 WHERE id = 2;",
    "  -- Both or neither — ACID guarantees",
    "COMMIT;  -- or ROLLBACK;",
    "",
    "-- ISOLATION LEVELS (PostgreSQL):",
    "SET TRANSACTION ISOLATION LEVEL READ COMMITTED;  -- default",
    "-- READ UNCOMMITTED: see uncommitted changes (dirty reads) — not used",
    "-- READ COMMITTED:   see only committed data. Default in PostgreSQL.",
    "-- REPEATABLE READ:  same query returns same rows within transaction",
    "-- SERIALIZABLE:     transactions behave as if run one at a time",
    "",
    "-- PHANTOM READS: a new row that matches your WHERE appears mid-transaction",
    "-- Transaction 1: SELECT COUNT(*) FROM orders WHERE amount > 1000 → 5",
    "-- Transaction 2: INSERT order with amount=1500, COMMIT",
    "-- Transaction 1: SELECT COUNT(*) FROM orders WHERE amount > 1000 → 6 (phantom!)",
    "-- Fix: use SERIALIZABLE isolation level",
    "```",
