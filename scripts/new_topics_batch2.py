    "## N+1 Problem and Lateral Joins",
    "",
    "```sql",
    "-- N+1: load 100 users, then for each user load their orders",
    "-- = 1 query + 100 queries = 101 DB round trips (TERRIBLE)",
    "",
    "-- Fix: eager load with JOIN",
    "SELECT u.id, u.name, o.id AS order_id, o.amount",
    "FROM users u",
    "LEFT JOIN orders o ON o.user_id = u.id",
    "WHERE u.id IN (1,2,3,...100);",
    "-- 1 query, returns all data",
    "",
    "-- LATERAL JOIN: like a correlated subquery that can reference outer table",
    "-- Get 3 most recent orders per user:",
    "SELECT u.name, recent.amount",
    "FROM users u",
    "CROSS JOIN LATERAL (",
    "  SELECT amount FROM orders",
    "  WHERE user_id = u.id",
    "  ORDER BY created_at DESC",
    "  LIMIT 3",
    ") AS recent;",
    "```",
